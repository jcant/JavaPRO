package compressor;

public class ArchiveFile {
    private String name;
    private byte[] data;

    public ArchiveFile() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public byte[] getData() {
        return data;
    }

    public void setData(byte[] data) {
        this.data = data;
    }
}
